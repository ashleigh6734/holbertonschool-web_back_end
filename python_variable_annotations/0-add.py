#!/usr/bin/env python3
"""
Module 0-add
Provides a type-annotated function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Return the sum of two float numbers.

    Args:
        a (float): First number
        b (float): Second number

    Returns:
        float: The sum of a and b
    """
    return a + b
